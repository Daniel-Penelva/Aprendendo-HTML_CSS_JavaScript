// Exemplo do arquivo ModuloImportExport.html 

/*
    Exportação nomeada (Named Export)
    Aqui, pode exportar várias funções, variáveis ou objetos de um mesmo arquivo.
*/

export function somar(a, b) {
  return a + b;
}

export function substract(a, b) {
  return a - b;
}

// Exportando constante
export const PI = 3.14159;
